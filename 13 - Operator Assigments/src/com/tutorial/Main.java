package com.tutorial;

public class Main {
  public static void main(String[] args) {

    // Operator Assignment
    // Berlaku untuk semua operator aritmatika penjumlahan,pengurangan,pembagian dan perkalian


    // 1. Assignment Operator Penjumlahan
    int a = 1;
    // a = a + 5;
    a += 10; // sama dengan a = a + 10;
    System.out.println("Nilai a = " + a);

    // 2. Assigment Operator Pengurangan
    int b = 100;
    b -= 10; // sama dengan a = a - 10;
    System.out.println("Nilai a = " + b);

    // 3. Assigment Operator Peembagian
    int c = 100;
    c /= 10; // sama dengan a = a / 10;
    System.out.println("Nilai a = " + c);

    // 4. Assigment Operator Modulus
    int d = 21;
    d %= 5; // sama dengan a = a % 10;
    System.out.println("Nilai a = " + d);


  }
}
