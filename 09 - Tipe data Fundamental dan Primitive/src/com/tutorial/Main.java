package com.tutorial;

public class Main {
  public static void main(String[] args) {
    // tipe data di java
    // integer, byte, short, long, foat, double, boolean

    // integer (satuan)
    int i = 10;
    System.out.println("======INTEGER======");
    System.out.println("Ini adalah nilai integer i = " + i);
    System.out.println("Nilai max integer = " + Integer.MAX_VALUE);
    System.out.println("Nilai max integer = " + Integer.MIN_VALUE);
    System.out.println("Besar integer = " + Integer.BYTES + "bytes");
    System.out.println("Besar integer = " + Integer.SIZE+ "bit");

    // byte (satuan)
    byte b = 10;
    System.out.println("======BYTE======");
    System.out.println("Ini adalah nilai byte b = " + b);
    System.out.println("Nilai max byte = " + Byte.MAX_VALUE);
    System.out.println("Nilai max byte = " + Byte.MIN_VALUE);
    System.out.println("Besar byte = " + Byte.BYTES + "bytes");
    System.out.println("Besar byte = " + Byte.SIZE+ "bit");

    // short (satuan)
    short c = 10;
    System.out.println("======SHORT======");
    System.out.println("Ini adalah nilai short c = " + c);
    System.out.println("Nilai max short = " + Short.MAX_VALUE);
    System.out.println("Nilai max short = " + Short.MIN_VALUE);
    System.out.println("Besar short = " + Short.BYTES + "bytes");
    System.out.println("Besar short = " + Short.SIZE+ "bit");

    // long (satuan)
    long d = 10L;
    System.out.println("======LONG======");
    System.out.println("Ini adalah nilai long d = " + d);
    System.out.println("Nilai max long = " + Long.MAX_VALUE);
    System.out.println("Nilai max long = " + Long.MIN_VALUE);
    System.out.println("Besar long = " + Long.BYTES + "bytes");
    System.out.println("Besar long = " + Long.SIZE+ "bit");

    // double (satuan)
    double e = -10.5d;
    System.out.println("======DOUBLE======");
    System.out.println("Ini adalah nilai double e = " + e);
    System.out.println("Nilai max double = " + Double.MAX_VALUE);
    System.out.println("Nilai max double = " + Double.MIN_VALUE);
    System.out.println("Besar double = " + Double.BYTES + "bytes");
    System.out.println("Besar double = " + Double.SIZE+ "bit");

    // float (satuan)
    float f = -8.5f;
    System.out.println("======FLOAT======");
    System.out.println("Ini adalah nilai float f = " + f);
    System.out.println("Nilai max float = " + Float.MAX_VALUE);
    System.out.println("Nilai max float = " + Float.MIN_VALUE);
    System.out.println("Besar float = " + Float.BYTES + "bytes");
    System.out.println("Besar float = " + Float.SIZE+ "bit");

    // char (koma, bilangan real) berdasarkan ASCII
    char C = 'c';
    System.out.println("======CHAR======");
    System.out.println("Ini adalah nilai char c = " + C);
    System.out.println("Nilai max char = " + Character.MAX_VALUE);
    System.out.println("Nilai max char = " + Character.MIN_VALUE);
    System.out.println("Besar char = " + Character.BYTES + "bytes");
    System.out.println("Besar char = " + Character.SIZE+ "bit");

    // boolean (nilainya true dan false)
    boolean val = true;
    System.out.println("======BOOLEAN======");
    System.out.println("Ini adalah nilai boolean val = " + val);
    System.out.println("Nilai max boolean = " + Boolean.TRUE);
    System.out.println("Nilai max boolean = " + Boolean.FALSE);
  }
}
